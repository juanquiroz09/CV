package app;

public class Experiencia {

  String habilidades;
  String conocimientos;
  String referencias;

  public void setHabilidades(String _habilidades) {
    habilidades = _habilidades;
  }

  public String getHabilidades() {
    return habilidades;
  }

  public void setConocimientos(String _conocimientos) {
    conocimientos = _conocimientos;
  }

  public String getConocimientos() {
    return conocimientos;
  }

  public void setReferencias(String _referencias) {
    referencias = _referencias;
  }

  public String getReferencias() {
    return referencias;
  }
}
